//Write a program that shows text representation of a day in a week for a number input from 1 to 7. Print output in console. For input 1 it should output Monday.

var day = 1;
var res = " ";
switch (day) {
    case 1:
        res = "Mon";
        console.log(res);

        break;

    case 2:
        res = "Tue";
        console.log(res);
        break;

    case 3:
        res = "Wed";
        console.log(res);
        break;

    case 4:
        res = "Thu";
        console.log(res);
        break;

    case 5:
        res = "Fri";
        console.log(res);
        break;

    case 6:
        res = "Sat";
        console.log(res);
        break;

    case 7:
        res = "Sun";
        console.log(res);
        break;

}